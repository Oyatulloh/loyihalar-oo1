# вызывать теперь функцию 'def'
def summa(a, b): # имя функция 'summa' и два параметра 'a, b'
    c = a + b # это тело функции
    print(c) # это тело функции, мы присываеваем результат суммирование a, b, далее выводим значение в терминал
a = 5
b = 3

summa(a, b) #8

a = 8
b = 12

summa(a, b) # 20


