def my_fn(a,b): # имя функция 'my_fn' параметры 'a,b'
    a = a+1 # значения этих параметров можно изменять, напр: a=a+1
    c = a+b
    return c # возврат результата 'return' если не напишите return c то, функция все равно вернёт результат! 
# ФУНКЦИЯ ВОЗВРАЩАЕТ NONE ЕСЛИ НЕТ КЛЮЧЕВОГО СЛОВА RETURN. ФУНКЦИЮ НУЖНО ВЫЗОВАТЬ ДЛЯ ВЫПОЛНЕНИЯ КОДА ВНУТРИ PYTHON.

# ниже объявление функции
def my_fn (a,b):
    a=a+1
    c=a+b
    return c # после return функция перекращает свою работу, это конец тело функции!

# ниже мы вызываем функции
print(my_fn(10, 3)) # аргументы 10, 3
 
# или можно и так написать
res = my_fn(10,3)
print(res)

